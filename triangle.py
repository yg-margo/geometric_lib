def area(a, h):
    """
    Вычисляет площадь треугольника по основанию и высоте.

    Использует формулу: S = a * h / 2

    Параметры:
       a (float): длина основания треугольника
       h (float): высота треугольника, проведённая к основанию a

    Возвращает:
       float: площадь треугольника

    Пример использования:
       >>> area(6, 4)
       12.0
       >>> area(10, 2.5)
       12.5
    """
    return a * h / 2

def perimeter(a, b, c):
    """
    Вычисляет периметр треугольника по известным длинам всех сторон.

    Использует формулу: P = a + b + c

    Параметры:
       a (float): длина первой стороны
       b (float): длина второй стороны
       c (float): длина третьей стороны

    Возвращает:
       float: периметр треугольника

    Пример использования:
       >>> perimeter(3, 4, 5)
       12
       >>> perimeter(7, 9, 12)
       28
    """
    return a + b + c